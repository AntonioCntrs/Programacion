#El dueno de una tienda compra un articulo a un precio determidado. determinar el precio en lo que lo debe vender para obtener una
#ganancia del 30%
precio=float(input("Dame el precio del articulo que desea vender"))
preciov=precio+(precio*.30)
print(f"El precio de venta para una ganancia de 30% es de {preciov}")